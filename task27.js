var alien_color = "green";
if (alien_color === "green") {
    console.log("Congrats You get 5 points");
}
else if (alien_color === "yellow") {
    console.log("Congrats You get 10 points");
}
else if (alien_color.toLowerCase() === "red") {
    console.log("Congrats! You get 15 points.");
}
else {
    console.log("The color is invalid!!");
}
